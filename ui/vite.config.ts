import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { defineConfig, type Plugin } from 'vite';
import { generateSprite } from './scripts/icons.ts';

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcRoot = resolve(__dirname, 'src');
const outRoot = resolve(__dirname, '../assets/js');
const banner = '/* @generated by ui vite build. Edit ui/src instead. */';

function generatedBanner(): Plugin {
  return {
    name: 'ghrm-generated-banner',
    generateBundle(_, bundle) {
      for (const artifact of Object.values(bundle)) {
        if (artifact.type === 'chunk') {
          artifact.code = `${banner}\n${artifact.code}`;
        }
      }
    },
  };
}

function iconSprite(): Plugin {
  return {
    name: 'ghrm-icon-sprite',
    async generateBundle() {
      this.emitFile({
        type: 'asset',
        fileName: 'icons.svg',
        source: await generateSprite(),
      });
    },
  };
}

const sharedModules = ['dom', 'prefs', 'vendor', 'status', 'runtime'];

function isSharedModule(id: string): boolean {
  for (const name of sharedModules) {
    if (id.endsWith(`/${name}.js`) || id.endsWith(`/${name}.ts`)) {
      return true;
    }
  }
  return false;
}

function isExplorerModule(id: string): boolean {
  return id.endsWith('/explorer.js') || id.endsWith('/explorer.ts');
}

export default defineConfig({
  plugins: [generatedBanner(), iconSprite()],
  build: {
    outDir: outRoot,
    emptyOutDir: true,
    lib: {
      entry: {
        preview: resolve(srcRoot, 'preview.ts'),
        main: resolve(srcRoot, 'main.ts'),
        gist: resolve(srcRoot, 'gist.ts'),
      },
      formats: ['es'],
      fileName: (_, name) => `${name}.js`,
    },
    rollupOptions: {
      output: {
        entryFileNames: '[name].js',
        chunkFileNames: 'chunks/[name].js',
        assetFileNames: '[name].[ext]',
        manualChunks: (id) => {
          if (id.includes('/adapters/')) {
            return 'adapters';
          }
          if (isExplorerModule(id)) {
            return 'explorer';
          }
          if (isSharedModule(id)) {
            return 'shared';
          }
          return undefined;
        },
      },
    },
    minify: false,
    sourcemap: false,
  },
});
